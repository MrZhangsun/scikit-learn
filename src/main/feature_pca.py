"""
为什么要降维？
降维的核心目的：在保留数据核心信息的前提下，减少特征数量。
高维空间中，样本之间距离趋同，难以区分

一、解决"维度灾难" (Curse of Dimensionality)
    随着维度增加，数据变得极其稀疏，带来一系列问题：
    1. 数据稀疏性
        # 示例：100个样本在10维空间中分布
        # 需要 10^10 个样本才能达到同样密度
        高维空间中，样本之间距离趋同，难以区分

    需要指数级增长的样本来维持统计显著性
    2. 过拟合风险急剧增加
        # 特征数 >> 样本数时
        X.shape = (100, 1000)  # 100个样本，1000个特征
        # 模型很容易"记住"噪声，而非学习
二、提升计算效率
    复杂度对比
    操作	        原始维度 p	降维后 k	    提速倍数
    距离计算	    O(p)	    O(k)	    p/k
    协方差矩阵	O(p²)	    O(k²)	    (p/k)²
    KNN查询	    O(np)	    O(nk)	    p/k
三、解决多重共线性
    问题：特征高度相关，导致模型不稳定
    降维解决：
        PCA 将相关特征合并成少数主成分
        消除特征间的线性依赖
        提高模型稳定性和可解释性
四、可视化需求
    人类无法理解超过3维的空间
    典型应用：
        数据探索（找聚类、异常值）
        结果展示（论文、报告）
        模式识别（人工验证分类效果）
五、去噪
    原理：噪声通常分布在方差小的方向上
    实际效果：
        图像去噪
        信号处理
        特征提纯



特征降维 - PCA（Principal Component Analysis）：
PCA（主成分分析）是机器学习中最经典、最常用的降维方法。在跨境电商场景中特别实用——比如你有
100个特征（价格、评分、点击率、转化率、退货率...），但很多特征是冗余的。
PCA降维：把多个相关特征，压缩成少数几个"综合特征"（主成分），尽量保留原始数据的信息（方差）。


一、PCA（主成分分析）
本质：一种无监督线性降维方法，目标是找到数据中方差最大的方向（主成分），将数据投影到这些方向上，实现降维。
核心思想：
保留数据中“信息量最大”（方差最大）的成分
去除冗余（相关性强的特征）和噪声（方差小的方向）
数学步骤：
数据中心化（减去均值）
计算协方差矩阵 C=1n−1XTX
对协方差矩阵做特征值分解：C=VΛVT
取特征值最大的 k 个特征向量作为投影基底
投影：Y=XVk
输出：主成分（特征向量）+ 方差解释比例（特征值）

五、一句话总结
PCA：面向数据分析的降维方法，求数据方差最大的方向。
SVD：通用的矩阵分解工具，可以稳定且高效地实现 PCA。
关系：PCA 本质上是将中心化数据矩阵进行 SVD 后取右奇异向量
在实际代码中（如 scikit-learn 的 PCA），底层就是通过 SVD 实现。

"""
import numpy as np
from sklearn.decomposition import PCA

X = np.array([
    [5.1, 3.5, 1.4, 0.2, 1, 23],
    [4.9, 3., 1.4, 0.2, 2.3, 2.1],
    [-6.2, 0.4, 5.4, 2.3, 2, 23],
    [-5.9, 0., 5.1, 1.8, 2, 3]
], dtype=np.float64)
"""
n_components: 可以取整数也可以设置浮点数，但是表示的含义会不一样：
整数：表示要保留的特征维度
浮点数：表示主成分方向的最小方差阈值
"""
pca = PCA(n_components=2)
pca.fit(X)

print("主成分方向（每个有6个权重）:")
print(pca.components_)
print(f"形状: {pca.components_.shape}\n")

print("各主成分方差值:")
print(pca.explained_variance_)
print(f"形状: {pca.explained_variance_.shape}\n")

"""
# 判断标准：
# - 累计方差 > 85%：较好
# - 累计方差 > 95%：优秀
# - 累累计方差 < 70%：信息损失较大
"""
print("方差解释比例:")
print(pca.explained_variance_ratio_)
print(f"累计方差: {pca.explained_variance_ratio_.sum():.2%}\n")

print("原始数据各特征均值:")
print(pca.mean_)
print(f"形状: {pca.mean_.shape}")

print("降维后的数据:")
print(pca.transform(X))
